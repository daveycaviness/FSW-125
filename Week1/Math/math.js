function addition(firstNum, secondNum){
    return firstNum + secondNum 
}

function subtraction(firstNum, secondNum){
    return firstNum - secondNum 
}

function multiplication(firstNum, secondNum){
    return firstNum * secondNum 
}

function division(firstNum, secondNum){
    return firstNum / secondNum 
}

module.exports = {
    addition,
    subtraction,
    multiplication,
    division
}

